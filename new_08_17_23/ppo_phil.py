    print("env.observation_space.shape)",env.observation_space.shape)
    N = 20
    batch_size = 5
    n_epochs = 4
    alpha = 0.0003
    agent = Agent(n_actions=env.action_space.n, batch_size=batch_size, 
                    alpha=alpha, n_epochs=n_epochs, 
                    input_dims=(4,*env.observation_space.shape) ) # (4, 210, 160, 3)
    n_games = 300

    figure_file = 'plots/cartpole.png'

    best_score = env.reward_range[0]
    score_history = []

    learn_iters = 0
    avg_score = 0
    n_steps = 0

    frame_buffer = deque(maxlen=4)
    for _ in range(4):
        frame_buffer.append(np.zeros(env.observation_space.shape))
    print("1. frame_buffer.shape",np.array(frame_buffer).shape)
    for i in range(n_games):
        #observation = env.reset()[0]
        ###print(observation)
        #exit()
        observation = env.reset()[0]
        for _ in range(4):  # Fill the buffer with the initial observation
            frame_buffer.append(observation)
        print("frame_buffer shape", np.array(frame_buffer).shape)
        done = False
        score = 0
        while not done:
            # Stack the last 4 frames together to form the input to the network
            stacked_frames = np.stack(frame_buffer, axis=0)
            action, prob, val = agent.choose_action(stacked_frames)
            #action, prob, val = agent.choose_action(observation)
            
            observation_, reward, done, truncated, info = env.step(action)
            frame_buffer.append(observation_)  # Add the new observation to the buffer
            n_steps += 1
            score += reward
            agent.remember(observation, action, prob, val, reward, done)
            if n_steps % N == 0:
                agent.learn()
                learn_iters += 1
            observation = observation_
        score_history.append(score)
        avg_score = np.mean(score_history[-100:])

        if avg_score > best_score:
            best_score = avg_score
            agent.save_models()

        print('episode', i, 'score %.1f' % score, 
              'time_steps', n_steps, 'learning_steps',
              learn_iters, '\t avg score %.1f' % avg_score,)
    x = [i+1 for i in range(len(score_history))]
    plot_learning_curve(x, score_history, figure_file)


